/*
Exercicio :
Faça um programa em JAVA para exibir todos os números ímpares entre 0 
e 100.
 */
package lista9;

public class Exercicio1_lista9 {

    public static void main(String[] args) {
        int i;
        System.out.println("0");
        for (i=1;i <=100; i=i+2)
            System.out.println(i);
    }
    
}
